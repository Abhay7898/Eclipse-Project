package constructor_concept;

public class ConstructorOverloading {

	public ConstructorOverloading() {
		System.out.println("no-arg-constructor");
	}

	public ConstructorOverloading(int i) {
		System.out.println("int-arg-constructor");
	}

	public ConstructorOverloading(long l) {
		System.out.println("long-arg-constructor");
	}

	public ConstructorOverloading(float f) {
		System.out.println("float-arg-constructor");
	}

	public ConstructorOverloading(double d) {
		System.out.println("double-arg-constructor");
	}

}
